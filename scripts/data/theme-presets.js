export function getAllThemes() {
  return THEME_CATEGORIES.flatMap(cat => cat.themes);
}

export function getTheme(id) {
  return getAllThemes().find(t => t.id === id) ?? getAllThemes()[0];
}

export function getThemeChoices() {
  const choices = {};
  for (const cat of THEME_CATEGORIES) {
    for (const theme of cat.themes) {
      choices[theme.id] = `${cat.label}: ${theme.label}`;
    }
  }
  return choices;
}
